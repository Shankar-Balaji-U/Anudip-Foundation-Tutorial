class ComparativeOperators {
	public static void main(String[] args) {
		/*
		==  -> Equal to
		!=  -> Not equal to
		<   -> Lesser than
		>   -> Greater than
		<=  -> Lesser than or equal to
		>=  -> Greater than or equal to
		*/

		int a = 6;
		int b = 4;
		System.out.println("if a = " + a + ", b = " + b + ".");
		System.out.println("\ta == b: " + (a == b));
		System.out.println("\ta != b: " + (a != b));
		System.out.println("\ta < b: " + (a < b));
		System.out.println("\ta > b: " + (a > b));
		System.out.println("\ta <= b: " + (a <= b));
		System.out.println("\ta >= b: " + (a >= b));
	}
}